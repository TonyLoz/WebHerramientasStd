public class ArchivoOpcionesBean {

	public String archivo;
	public String colectivo;
	public boolean foliar;
	public boolean certFinal;
	public boolean certTodo;	
	
	/**
	 * @return the archivo
	 */
	public String getArchivo() {
		return archivo;
	}
	/**
	 * @param archivo the archivo to set
	 */
	public void setArchivo(String archivo) {
		this.archivo = archivo;
	}
	/**
	 * @return the colectivo
	 */
	public String getColectivo() {
		return colectivo;
	}
	/**
	 * @param colectivo the colectivo to set
	 */
	public void setColectivo(String colectivo) {
		this.colectivo = colectivo;
	}
	/**
	 * @return the foliar
	 */
	public boolean isFoliar() {
		return foliar;
	}
	/**
	 * @param foliar the foliar to set
	 */
	public void setFoliar(boolean foliar) {
		this.foliar = foliar;
	}
	/**
	 * @return the certFinal
	 */
	public boolean isCertFinal() {
		return certFinal;
	}
	/**
	 * @param certFinal the certFinal to set
	 */
	public void setCertFinal(boolean certFinal) {
		this.certFinal = certFinal;
	}
	/**
	 * @return the certTodo
	 */
	public boolean isCertTodo() {
		return certTodo;
	}
	/**
	 * @param certTodo the certTodo to set
	 */
	public void setCertTodo(boolean certTodo) {
		this.certTodo = certTodo;
	}



}
